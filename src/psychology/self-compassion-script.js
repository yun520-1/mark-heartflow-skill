/**
 * 自我慈悲脚本 - 具体可执行的冥想/话语引导
 */
const selfCompassionScript = {
  // 生成完整慈悲话语
  generateCompassionScript(situation = '') {
    const lines = [
      '=== 自我慈悲练习 ===',
      '',
      situation ? `情境："${situation}"` : '现在，让我们对自己说一些善意的话。',
      '',
      '第一步：承认痛苦',
      '"这是痛苦的时刻。"',
      '"痛苦是生活的一部分。"',
      '',
      '第二步：承认共通人性',
      '"每个人的生活都有艰难时刻。"',
      '"我并不孤单。"',
      '',
      '第三步：对自己说善意的话',
      '"我愿意对自己温柔。"',
      '"我值得被善待。"',
      '"我接受自己不完美的事实。"',
      '',
      '第四步：身体慈悲（可选）',
      '把手放在心口，或任何让你感到温暖的地方。',
      '感受手的温度，想象温暖从手掌传递到心口。',
      '对自己说："我接纳此刻的自己。"',
      '',
      '结束语：',
      '"我愿意记住：我是完整的人，不是完美的机器。"',
      '"我的价值不取决于我的表现。"'
    ];
    return lines.join('\n');
  },

  // 短版慈悲话语（用于情绪发作时）
  getQuickCompassion() {
    return [
      '=== 紧急慈悲话语 ===',
      '',
      '现在对自己说：',
      '',
      '① "这是痛苦的时刻。"（承认）',
      '② "这是人类共同的经历。"（共通）',
      '③ "我愿意对自己温柔。"（善意）',
      '',
      '把手放在心口，说：',
      '"我接纳此刻的自己。我已经足够了。"'
    ].join('\n');
  }
};

module.exports = { selfCompassionScript };
